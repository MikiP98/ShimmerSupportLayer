package io.github.mikip98.automation.modSupport;

import io.github.mikip98.automation.structures.SupportedMod;

import java.util.ArrayList;

public class HandMadeSupport {
    // This Class is for mods that have full hand made or hand tweaked support.
    // Every color has been hand-picked and tweaked to look as good as possible.
    // All duplicate colors have been removed.
    // Some blocks and items may have been disabled if they are not compatible or too performance intensive.
    // This is the best NOT NATIVE support you can get for a mod.

    public static ArrayList<SupportedMod> getSupportedMods() {
        ArrayList<SupportedMod> supportedMods = new ArrayList<>();

        return supportedMods;
    }
}
